
# Генерируем строку с символами алфавита в верхнем и нижнем регистре, а также числами
text = "Hello world in 2023"

# Выводим первый символ текста
print("Первый символ текста:", text[0])

# Выводим последний символ текста
print("Последний символ текста:", text[-1])

# Выводим 3-й символ с начала и 3-й символ с конца
print("3-й символ с начала:", text[2])
print("3-й символ с конца:", text[-3])

# Создаем срез из первых 8 символов
slice_first_8 = text[:8]
print("Срез из первых 8 символов:", slice_first_8)

# Выводим только символы с индексом, который делится на 3 без остатка
divisible_by_3 = text[::3]
print("Символы с индексом, делющимся на 3:", divisible_by_3)

# Выводим символ, находящийся посередине строки
middle_index = len(text) // 2
middle_symbol = text[middle_index]
print("Символ посередине строки:", middle_symbol)

# Реверсируем текст с помощью среза
reversed_text = text[::-1]
print("Реверсированный текст:", reversed_text)
